local lazypath = vim.fn.stdpath("data") .. "/lazy/lazy.nvim"
if not vim.loop.fs_stat(lazypath) then
  vim.fn.system({
    "git",
    "clone",
    "--filter=blob:none",
    "https://github.com/folke/lazy.nvim.git",
    "--branch=stable",
    lazypath,
  })
end
vim.opt.rtp:prepend(lazypath)

-- This tells Lazy to look for plugin specs in the "lua/plugins" directory
require("lazy").setup("plugins", {
    change_detection = { notify = false }, -- specific to silence unnecessary notifications
})
